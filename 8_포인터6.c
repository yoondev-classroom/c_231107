// 8_포인터6.c
#include <stdio.h>

int main(void)
{
    // int* p2 = 100; /* 에러! */

    // 정수 0은 모든 포인터 타입으로의 암묵적인 변환을 허용합니다.

    int* p = NULL;
    printf("%p\n", p);
    // 포인터는 유효한 값인지 유효하지 않은 값인지 구분하기 어렵습니다.
    // 사용하지 않는 포인터(유효하지 않은 포인터)는 0으로 초기화해야 합니다.

    // 포인터 참조를 하기 전에, 반드시 유효성 여부를 확인해야 합니다.
    if (p != NULL) {
        *p = 42;
    }

    if (p) {
        *p = 42;
    }

    return 0;
}
